package org.zstack.sdk.iam2.api;



public class ErrorResult {

    public int line;
    public void setLine(int line) {
        this.line = line;
    }
    public int getLine() {
        return this.line;
    }

    public java.lang.String detail;
    public void setDetail(java.lang.String detail) {
        this.detail = detail;
    }
    public java.lang.String getDetail() {
        return this.detail;
    }

}
